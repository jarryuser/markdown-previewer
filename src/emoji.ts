export const markedEmoji = {
  extensions: [{
    name: 'emoji',
    level: 'inline' as const,
    start(src: string) {
      return src.indexOf(':');
    },
    tokenizer(this: any, src: string) {
      const match = src.match(/^:([a-z0-9_+-]+):/);
      if (match && GITHUB_EMOJI[match[1]]) {
        return {
          type: 'emoji',
          raw: match[0],
          emoji: GITHUB_EMOJI[match[1]],
        };
      }
      return undefined;
    },
    renderer(token: any) {
      return token.emoji;
    },
  }],
};
